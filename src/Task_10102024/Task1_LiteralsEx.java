package Task_10102024;

public class Task1_LiteralsEx{

    public static void main(String[] args) {

        // integer literals
        int length = 10; // Integer literal for length
        int breadth = 5; // Integer literal for breadth
        int area = length * breadth; // Calculating area
        System.out.println("Area of the rectangle: " + area);


        // floating literals
        float speed=10.5f;
        float time=2.5f;
        float distance = speed * time ;
        System.out.println("distance "+distance);

        //   Character Literals

        char grade = 'A'; // Character literal for grade
        System.out.println("Grade: " + grade);
        //  String Literals
        String name ="Balaji Chandran";
        String message= "Welcome to Automation World ";
        System.out.println(message + name);

        //  Boolean Literals
        boolean is_automation_tough =false;
        System.out.println(is_automation_tough);


    }

}

